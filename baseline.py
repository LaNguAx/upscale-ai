def extract_segment(video_path, start_ms, split_name, clip_name, num_frames=160, scale=4):
    cap = cv2.VideoCapture(str(video_path))
    cap.set(cv2.CAP_PROP_POS_MSEC, start_ms)

    hr_dir = SPLIT_ROOT / split_name / clip_name / "hr_frames"
    lr_dir = SPLIT_ROOT / split_name / clip_name / "lr_frames"

    count = 0
    while count < num_frames:
        ret, frame = cap.read()
        if not ret:
            break

        hr_file = hr_dir / f"{count:04d}.png"
        lr_file = lr_dir / f"{count:04d}.png"

        cv2.imwrite(str(hr_file), frame)

        lr_frame = cv2.resize(
            frame,
            (frame.shape[1] // scale, frame.shape[0] // scale),
            interpolation=cv2.INTER_CUBIC
        )
        cv2.imwrite(str(lr_file), lr_frame)

        count += 1

    cap.release()
    print(f"Finished {split_name}/{clip_name} from {video_path.name} with {count} frames")

