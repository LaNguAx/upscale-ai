def make_demo_lr_frame(frame_bgr, scale=4):
    h, w = frame_bgr.shape[:2]

    small = cv2.resize(
        frame_bgr,
        (w // scale, h // scale),
        interpolation=cv2.INTER_CUBIC
    )

    return small

