def train_one_epoch(model, loader, optimizer, criterion, device):
    model.train()
    running_loss = 0.0

    for lr_seq, hr_seq in loader:
        lr_seq = lr_seq.to(device, non_blocking=True)
        hr_seq = hr_seq.to(device, non_blocking=True)

        target = hr_seq[:, hr_seq.shape[1] // 2]

        optimizer.zero_grad(set_to_none=True)
        pred = model(lr_seq)
        loss = criterion(pred, target)
        loss.backward()
        optimizer.step()

        running_loss += loss.item()

    return running_loss / max(len(loader), 1)


@torch.no_grad()
def evaluate_patch_loader(model, loader, criterion, device):
    model.eval()
    running_loss = 0.0
    running_psnr = 0.0

    for lr_seq, hr_seq in loader:
        lr_seq = lr_seq.to(device, non_blocking=True)
        hr_seq = hr_seq.to(device, non_blocking=True)

        target = hr_seq[:, hr_seq.shape[1] // 2]
        pred = model(lr_seq)

        loss = criterion(pred, target)
        psnr = psnr_torch(pred, target)

        running_loss += loss.item()
        running_psnr += psnr

    avg_loss = running_loss / max(len(loader), 1)
    avg_psnr = running_psnr / max(len(loader), 1)
    return avg_loss, avg_psnr


@torch.no_grad()
def evaluate_full_frame_loader(model, loader, device):
    model.eval()
    psnrs = []

    for lr_seq, hr_seq, clip_name, start_idx in loader:
        lr_seq = lr_seq.to(device, non_blocking=True)   # [1, T, C, H, W]
        hr_seq = hr_seq.to(device, non_blocking=True)

        target = hr_seq[:, hr_seq.shape[1] // 2]
        pred = model(lr_seq)

        psnrs.append(psnr_torch(pred, target))

    return float(np.mean(psnrs)) if len(psnrs) > 0 else 0.0

