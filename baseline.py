@torch.no_grad()
def evaluate_bicubic_full_frame(loader, scale=4):
    psnrs = []

    for lr_seq, hr_seq, clip_name, start_idx in loader:
        lr_mid = lr_seq[:, lr_seq.shape[1] // 2]   # [1, C, H, W]
        hr_mid = hr_seq[:, hr_seq.shape[1] // 2]   # [1, C, H, W]

        bicubic = F.interpolate(
            lr_mid,
            scale_factor=scale,
            mode="bicubic",
            align_corners=False
        )

        h = min(bicubic.shape[-2], hr_mid.shape[-2])
        w = min(bicubic.shape[-1], hr_mid.shape[-1])

        bicubic = bicubic[..., :h, :w]
        hr_mid = hr_mid[..., :h, :w]

        psnrs.append(psnr_torch(bicubic, hr_mid))

    return float(np.mean(psnrs)) if len(psnrs) > 0 else 0.0

